# Tipos INT e FLOAT
# int -> Números inteiros
# O tipo int representa números inteiros, positivos ou negativos, sem parte decimal.
print("----------------")
print("Tipos INT")
print(11)
print(-11)
print(0)
print("----------------")

# float -> Números de ponto flutuante
# O tipo float representa números reais, ou seja, números com parte decimal.

print("Tipos FLOAT")
print(1.1)
print(-1.5)
print(0.0)
print("----------------")

# Função type()
# A função type() é usada para verificar o tipo de dado de uma variável ou valor.
print("Verificando tipos com type()")
print(type(11))     # Saída: <class 'int'>
print(type(-11))    # Saída: <class 'int'>
print(type(0))      # Saída: <class 'int'>
print(type(1.1))    # Saída: <class 'float'>
print(type('Ola'))  # Saída: <class 'str'>