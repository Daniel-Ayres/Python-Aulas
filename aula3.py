print(1234)

# Aspas simples
print('Ola, Mundo!')

# Aspas duplas
print("Ola, Mundo!")


# Escape
print('Ola, \'Mundo\'!')  # Usando aspas simples dentro de aspas simples

# r
print(r'Ola, \'Mundo\'!')  # String raw (crua), ignora caracteres de escape


# Aspas simples dentro de aspas duplas
print("Ola, 'Mundo'!")  # Usando aspas simples dentro de aspas duplas